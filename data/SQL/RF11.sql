SELECT * FROM (SELECT CLIENTE.ID, CLIENTE.NOMBRE, TRUNC(FACTURA.FECHA) AS FECHA, SUM (FACTURAPRODUCTO.UNIVENDIDAS)  AS SUMA_TOT
FROM CLIENTE
FULL OUTER JOIN FACTURA ON CLIENTE.ID = FACTURA.IDCLIENTE
FULL OUTER JOIN FACTURAPRODUCTO ON FACTURA.ID = FACTURAPRODUCTO.IDFACTURA
GROUP BY CLIENTE.ID, CLIENTE.NOMBRE, TRUNC(FACTURA.FECHA)
MINUS (SELECT CLIENTE.ID, CLIENTE.NOMBRE, TRUNC(FACTURA.FECHA), SUM (FACTURAPRODUCTO.UNIVENDIDAS)  AS SUMA_TOT
FROM CLIENTE
FULL OUTER JOIN FACTURA ON CLIENTE.ID = FACTURA.IDCLIENTE
FULL OUTER JOIN FACTURAPRODUCTO ON FACTURA.ID = FACTURAPRODUCTO.IDFACTURA
WHERE FACTURA.FECHA BETWEEN '01/01/2018' AND '31/12/2018'
GROUP BY CLIENTE.ID, CLIENTE.NOMBRE, TRUNC(FACTURA.FECHA)))

